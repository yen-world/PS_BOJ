g = int(input())

if 620 <= g <= 780 :
    print("Red")
elif 590 <= g < 620 :
    print("Orange")
elif 570 <= g < 590 :
    print("Yellow")
elif 495 <= g < 570 :
    print("Green")
elif 450 <= g < 495 :
    print("Blue")
elif 425 <= g < 450 :
    print("Indigo")
elif 380 <= g < 425 :
    print("Violet")